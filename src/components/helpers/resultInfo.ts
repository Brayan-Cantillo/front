export const cases = (value: string,bodyChoose:string,casesChoose:string ): boolean => {
  // R = constante resorte
  // D = deflexion
  // DFMINMAX = deflexion max y minima
  // FMINMAX = fuerza minima y maxima
  // DEX = Dexterno
  // LF = longitud libre
  // MMINMAX = moneto minimo y maximo
  // AMAXMIN = angulo maximo y angulo minimo
  if (bodyChoose === "Compresión") {
    if (casesChoose === "1") {
      if (value === "D") return true;
      if (value === "FMINMAX") return true;
    }
    if (casesChoose === "2") {
      if (value === "R") return true;
      if (value === "FMINMAX") return true;
    }
    if (casesChoose === "3") {
      if (value === "R") return true;
      if (value === "DFMINMAX") return true;
    }
    if (casesChoose === "4") {
      if (value === "R") return true;
      if (value === "FMINMAX") return true;
      if (value === "DEX") return true;
      if (value === "LF") return true;
    }
    if (casesChoose === "5") {
      if (value === "R") return true;
      if (value === "DFMINMAX") return true;
      if (value === "DEX") return true;
      if (value === "LF") return true;
    }
  }

  if (bodyChoose === "Extensión") {
    if (casesChoose === "1") {
      if (value === "D") return true;
      if (value === "FMINMAX") return true;
    }
    if (casesChoose === "2") {
      if (value === "R") return true;
      if (value === "FMINMAX") return true;
    }
    if (casesChoose === "3") {
      if (value === "R") return true;
      if (value === "DFMINMAX") return true;
    }
    if (casesChoose === "4") {
      if (value === "FMINMAX") return true;
      if (value === "DEX") return true;
      if (value === "LF") return true;
      if (value === "R") return true;
    }
    if (casesChoose === "5") {
      if (value === "R") return true;
      if (value === "DEX") return true;
      if (value === "LF") return true;
      if (value === "DFMINMAX") return true;
    }
  }
  if (bodyChoose === "Torsión") {
    if (casesChoose === "1") {
      if (value === "MMINMAX") return true;
    }
    if (casesChoose === "2") {
      if (value === "R") return true;
      if (value === "MMINMAX") return true;
    }
    if (casesChoose === "3") {
      if (value === "R") return true;
      if (value === "AMAXMIN") return true;
    }
    if (casesChoose === "4") {
      if (value === "R") return true;
      if (value === "MMINMAX") return true;
      if (value === "DEX") return true;
    }
    if (casesChoose === "5") {
      if (value === "R") return true;
      if (value === "AMAXMIN") return true;
      if (value === "DEX") return true;
    }
  }
  return false;
};

